def generate_title_update_sql():
    """실제 찬송가 제목으로 업데이트하는 SQL 생성"""

    print("-- 실제 새찬송가 제목으로 업데이트")
    print("BEGIN;")

    for number, title in REAL_HYMN_TITLES.items():
        print(f"UPDATE hymns SET title = '{title}' WHERE number = {number};")

    print("COMMIT;")
    print(f"\n-- 총 {len(REAL_HYMN_TITLES)}곡 제목 업데이트됨")

def generate_import_json():
    """실제 제목을 반영한 import JSON 생성"""
    import json

    updates = []
    for number, title in REAL_HYMN_TITLES.items():
        updates.append({
            "number": number,
            "title": title,
            "lyrics": f"{title}\n\n[예배용 웹앱]\n비영리 교육 목적\n\n실제 가사는 저작권 보호를 위해\n한국찬송가공회 공식 자료를\n참조하시기 바랍니다.",
            "theme": "예배",
            "composer": "새찬송가",
            "author": "새찬송가",
            "tempo": "Moderate",
            "key_signature": "C",
            "bible_reference": "시편 150:6"
        })

    with open('real_hymn_titles_update.json', 'w', encoding='utf-8') as f:
        json.dump(updates, f, ensure_ascii=False, indent=2)

    print(f"✅ real_hymn_titles_update.json 생성 완료 ({len(updates)}곡)")

